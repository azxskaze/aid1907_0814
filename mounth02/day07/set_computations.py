'''集合的运算/交并补差'''
set01=set('abcde')
set02=set('defgh')

print(set01&set02)#'''交集'''
print(set01|set02)#'''并集'''
print(set01.difference(set02))#'''补集'''
print(set01-set02)#'''补集'''
print(set02.difference(set01))#'''输出集合1中有但集合2中没有的'''
print(set02-set01)#'''输出集合2中有但集合1中没有的'''
print(set01.symmetric_difference(set02))#'''交叉补集，并集-交集-交集''''''交叉补集'''
print(set01^set02)
print(((set01|set02)-(set01&set02)-(set01&set02)))
set03={1,2}
set04={1,2,3}
print(set03<set04)#'''子集set03是set04的子集'''
print(set04>set03)#'''超集set04是set03的超集'''
print(set03==set04)#'''判断是否完全相等'''
print(set03!=set04)
